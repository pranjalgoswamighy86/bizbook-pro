// Apply imported data to database
// ============================================

async function applyImportData(tenantId: string, importData: any) {
  const results: any = { sales: 0, purchases: 0, expenses: 0, products: 0, parties: 0, staff: 0, bankTransactions: 0, errors: [] as string[] }

  try {
    // Parties — upsert by name
    if (importData.parties?.length > 0) {
      for (const party of importData.parties) {
        try {
          const existing = await db.party.findFirst({ where: { name: party.name || 'Unknown Party', tenantId, isDeleted: false } })
          if (existing) {
            await db.party.update({ where: { id: existing.id }, data: {
              phone: party.phone ?? existing.phone,
              email: party.email ?? existing.email,
              address: party.address ?? existing.address,
              gstNumber: (party.gstNumber || party.gstin) ?? existing.gstNumber,
              panNumber: party.panNumber ?? existing.panNumber,
              type: party.type ?? existing.type,
            } })
          } else {
            await db.party.create({ data: {
              name: party.name || 'Unknown Party',
              type: party.type || 'BOTH',
              phone: party.phone || null,
              email: party.email || null,
              address: party.address || null,
              gstNumber: party.gstNumber || party.gstin || null,
              panNumber: party.panNumber || null,
              openingBalance: Number(party.openingBalance || 0),
              currentBalance: Number(party.openingBalance || 0),
              tenantId,
            } })
          }
          results.parties++
        } catch (e: any) { results.errors.push(`Party "${party.name}": ${e.message}`) }
      }
    }

    // Products — upsert by name
    if (importData.products?.length > 0) {
      for (const product of importData.products) {
        try {
          const normalized = normalizeProductData(product)
          const existing = await db.inventoryItem.findFirst({ where: { name: normalized.name, tenantId, isDeleted: false } })
          if (existing) {
            await db.inventoryItem.update({ where: { id: existing.id }, data: {
              sku: normalized.sku ?? existing.sku,
              category: normalized.category ?? existing.category,
              hsnCode: normalized.hsnCode ?? existing.hsnCode,
              unit: normalized.unit ?? existing.unit,
              brand: normalized.brand ?? existing.brand,
              purchasePrice: normalized.purchasePrice ?? existing.purchasePrice,
              salePrice: normalized.salePrice ?? existing.salePrice,
              mrp: normalized.mrp ?? existing.mrp,
              gstRate: normalized.gstRate ?? existing.gstRate,
              currentStock: normalized.currentStock ?? existing.currentStock,
            } })
          } else {
            await db.inventoryItem.create({ data: { ...normalized, tenantId } })
          }
          results.products++
        } catch (e: any) { results.errors.push(`Product "${product.name}": ${e.message}`) }
      }
    }

    // Sales — skip duplicates by invoice number
    if (importData.sales?.length > 0) {
      for (const sale of importData.sales) {
        try {
          const normalized = normalizeSaleData(sale)
          const existing = await db.sale.findFirst({ where: { invoiceNumber: normalized.invoiceNumber, tenantId, isDeleted: false } })
          if (existing) { results.errors.push(`Sale "${normalized.invoiceNumber}": Already exists, skipped`); continue }
          await db.sale.create({ data: { ...normalized, tenantId } })
          results.sales++
        } catch (e: any) { results.errors.push(`Sale "${sale.invoiceNumber}": ${e.message}`) }
      }
    }

    // Purchases — skip duplicates by invoice number
    if (importData.purchases?.length > 0) {
      for (const purchase of importData.purchases) {
        try {
          const normalized = normalizePurchaseData(purchase)
          const existing = await db.purchase.findFirst({ where: { invoiceNumber: normalized.invoiceNumber, tenantId, isDeleted: false } })
          if (existing) { results.errors.push(`Purchase "${normalized.invoiceNumber}": Already exists, skipped`); continue }
          await db.purchase.create({ data: { ...normalized, tenantId } })
          results.purchases++
        } catch (e: any) { results.errors.push(`Purchase "${purchase.invoiceNumber}": ${e.message}`) }
      }
    }

    // Expenses
    if (importData.expenses?.length > 0) {
      for (const expense of importData.expenses) {
        try {
          const normalized = normalizeExpenseData(expense)
          await db.expense.create({ data: { ...normalized, tenantId } })
          results.expenses++
        } catch (e: any) { results.errors.push(`Expense "${expense.description}": ${e.message}`) }
      }
    }

    // Staff
    if (importData.staff?.length > 0) {
      for (const s of importData.staff) {
        try {
          const normalized = normalizeStaffData(s)
          await db.staff.create({ data: { ...normalized, tenantId } })
          results.staff++
        } catch (e: any) { results.errors.push(`Staff "${s.name}": ${e.message}`) }
      }
    }

    // Bank Transactions
    if (importData.bankTransactions?.length > 0) {
      for (const txn of importData.bankTransactions) {
        try {
          const normalized = normalizeBankTxnData(txn)
          await db.bankTransaction.create({ data: { ...normalized, tenantId } })
          results.bankTransactions++
        } catch (e: any) { results.errors.push(`Bank txn: ${e.message}`) }
      }
    }

    const totalImported = results.sales + results.purchases + results.expenses + results.products + results.parties + results.staff + results.bankTransactions
    return NextResponse.json({ success: true, message: `Imported ${totalImported} records successfully`, results })
  } catch (error: any) {
    console.error('Apply import error:', error)
    return NextResponse.json({ success: false, error: error.message, results }, { status: 500 })
  }
}

