async function parseExcelFile(buffer: Buffer): Promise<string> {
  try {
    const XLSX = await import('xlsx')
    const workbook = XLSX.read(buffer, { type: 'buffer' })
    const sheetsData: Record<string, any> = {}
    for (const sheetName of workbook.SheetNames) {
      const sheet = workbook.Sheets[sheetName]
      const jsonData = XLSX.utils.sheet_to_json(sheet, { defval: '' })
      if (jsonData.length > 0) {
        sheetsData[sheetName] = {
          headers: Object.keys(jsonData[0] as Record<string, unknown>),
          totalRows: jsonData.length,
          data: jsonData.length <= 500 ? jsonData : jsonData.slice(0, 500),
          truncated: jsonData.length > 500,
        }
      } else {
        sheetsData[sheetName] = { headers: [], totalRows: 0, data: [] }
      }
    }
    return JSON.stringify(sheetsData, null, 2)
  } catch (error: any) {
    console.error('[AI-Import] Excel parse error:', error.message)
    return `[Excel parsing failed: ${error.message}]`
  }
}

async function parseWordFile(buffer: Buffer): Promise<string> {
  try {
    const mammoth = await import('mammoth')
    const result = await mammoth.extractRawText({ buffer })
    return result.value || '[Empty document]'
  } catch (error: any) {
    console.error('[AI-Import] Word parse error:', error.message)
    try { return buffer.toString('utf-8').replace(/[\x00-\x08\x0B\x0C\x0E-\x1F]/g, '') || '[Could not parse]' } catch { return '[Word parsing failed]' }
  }
}

async function parsePdfFile(buffer: Buffer): Promise<string> {
  try {
    // pdf-parse v5+ uses named export PDFParse (class), not default
    const pdfModule = await import('pdf-parse')
    const PDFParse = pdfModule.PDFParse || pdfModule.default
    if (!PDFParse) throw new Error('PDFParse not found in module')
    const parser = new PDFParse()
    const data = await parser.parseBuffer(buffer)
    return data.text || '[Empty PDF]'
  } catch (error: any) {
    console.error('[AI-Import] PDF parse error:', error.message)
    // Fallback: try the old-style import
    try {
      const pdfModule = await import('pdf-parse')
      const pdfParse = pdfModule.default || pdfModule
      if (typeof pdfParse === 'function') {
        const data = await pdfParse(buffer)
        return data.text || '[Empty PDF]'
      }
    } catch {}
    return `[PDF text extraction failed: ${error.message}]`
  }
}

function parseCSVToStructured(csv: string, isTSV: boolean): string {
  const separator = isTSV ? '\t' : ','
  const lines = csv.split('\n').filter(l => l.trim())
  if (lines.length === 0) return ''
  const parseRow = (row: string): string[] => {
    const result: string[] = []
    let current = '', inQuotes = false
    for (let i = 0; i < row.length; i++) {
      const ch = row[i]
      if (ch === '"') { if (inQuotes && i + 1 < row.length && row[i + 1] === '"') { current += '"'; i++ } else { inQuotes = !inQuotes } }
      else if (ch === separator && !inQuotes) { result.push(current.trim()); current = '' }
      else { current += ch }
    }
    result.push(current.trim())
    return result
  }
  const headers = parseRow(lines[0])
  const data = lines.slice(1).map(parseRow)
  return JSON.stringify({ headers, totalRows: data.length, sampleRows: data.slice(0, 200), allRows: data.length <= 200 ? data : undefined }, null, 2)
}

