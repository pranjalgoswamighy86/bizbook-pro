async function exportMergedExcel(tenantId: string, importData: any) {
  try {
    const XLSX = await import('xlsx')
    const tenant = await db.tenant.findUnique({ where: { id: tenantId } })
    const wb = XLSX.utils.book_new()

    if (importData.parties?.length > 0) {
      const ws = XLSX.utils.json_to_sheet(importData.parties.map((p: any) => ({ Name: p.name, Type: p.type, Phone: p.phone, Email: p.email, GSTIN: p.gstNumber || p.gstin, Address: p.address, OpeningBalance: p.openingBalance })))
      XLSX.utils.book_append_sheet(wb, ws, 'Parties')
    }
    if (importData.products?.length > 0) {
      const ws = XLSX.utils.json_to_sheet(importData.products.map((p: any) => ({ Name: p.name, SKU: p.sku || p.barcode, Category: p.category, HSN: p.hsnCode || p.hsn, Unit: p.unit, PurchasePrice: p.purchasePrice || p.purchaseRate, SalePrice: p.salePrice || p.saleRate, MRP: p.mrp, GSTRate: p.gstRate, Stock: p.currentStock || p.stock, Brand: p.brand })))
      XLSX.utils.book_append_sheet(wb, ws, 'Products')
    }
    if (importData.sales?.length > 0) {
      const ws = XLSX.utils.json_to_sheet(importData.sales.map((s: any) => ({ Date: s.date, InvoiceNo: s.invoiceNumber, Party: s.partyName, Subtotal: s.subtotal || s.total, GST: s.gstAmount || s.gstTotal, Total: s.totalAmount || s.grandTotal, PaymentStatus: s.paymentStatus || s.status, Items: typeof s.items === 'string' ? s.items : JSON.stringify(s.items || []) })))
      XLSX.utils.book_append_sheet(wb, ws, 'Sales')
    }
    if (importData.purchases?.length > 0) {
      const ws = XLSX.utils.json_to_sheet(importData.purchases.map((p: any) => ({ Date: p.date, InvoiceNo: p.invoiceNumber, Party: p.partyName, Subtotal: p.subtotal || p.total, GST: p.gstAmount || p.gstTotal, Total: p.totalAmount || p.grandTotal, PaymentStatus: p.paymentStatus || p.status, Items: typeof p.items === 'string' ? p.items : JSON.stringify(p.items || []) })))
      XLSX.utils.book_append_sheet(wb, ws, 'Purchases')
    }
    if (importData.expenses?.length > 0) {
      const ws = XLSX.utils.json_to_sheet(importData.expenses.map((e: any) => ({ Date: e.date, Description: e.description, Category: e.category, Amount: e.amount, PaymentMode: e.paymentMode, Reference: e.reference })))
      XLSX.utils.book_append_sheet(wb, ws, 'Expenses')
    }
    if (importData.bankTransactions?.length > 0) {
      const ws = XLSX.utils.json_to_sheet(importData.bankTransactions.map((t: any) => ({ Date: t.date, Description: t.description, Deposit: t.deposit || (t.type === 'CREDIT' ? t.amount : 0), Withdrawal: t.withdrawal || (t.type === 'DEBIT' ? t.amount : 0), Balance: t.balance, Reference: t.reference, Category: t.category })))
      XLSX.utils.book_append_sheet(wb, ws, 'Bank Transactions')
    }
    if (importData.staff?.length > 0) {
      const ws = XLSX.utils.json_to_sheet(importData.staff.map((s: any) => ({ Name: s.name, Phone: s.phone, Email: s.email, Role: s.role, Department: s.department, Salary: s.salary })))
      XLSX.utils.book_append_sheet(wb, ws, 'Staff')
    }

    // If no sheets, create empty summary
    if (wb.SheetNames.length === 0) {
      const ws = XLSX.utils.aoa_to_sheet([['No data to export']])
      XLSX.utils.book_append_sheet(wb, ws, 'Summary')
    }

    const excelBuffer = XLSX.write(wb, { type: 'buffer', bookType: 'xlsx' })
    const fileName = `${tenant?.name || 'BizBook'}_AI_Import.xlsx`

    return new NextResponse(excelBuffer, {
      status: 200,
      headers: {
        'Content-Type': 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet',
        'Content-Disposition': `attachment; filename="${encodeURIComponent(fileName)}"`,
      },
    })
  } catch (error: any) {
    console.error('[AI-Import] Excel export error:', error)
    return NextResponse.json({ error: error.message || 'Failed to export Excel' }, { status: 500 })
  }
}
