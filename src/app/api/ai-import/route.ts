function normalizeSaleData(sale: any) {
  return {
    date: sale.date ? new Date(sale.date) : new Date(),
    invoiceNumber: sale.invoiceNumber || `AI-${Date.now()}-${Math.random().toString(36).slice(2, 6)}`,
    partyName: sale.partyName || 'Walk-in Customer',
    partyAddress: sale.partyAddress || sale.partyAddress || null,
    partyGst: sale.partyGst || sale.partyGstin || null,
    items: typeof sale.items === 'string' ? sale.items : JSON.stringify(sale.items || []),
    subtotal: Number(sale.subtotal || sale.total || 0),
    gstAmount: Number(sale.gstAmount || sale.gstTotal || 0),
    totalAmount: Number(sale.totalAmount || sale.grandTotal || sale.total || 0),
    paymentStatus: normalizeSalePaymentStatus(sale.paymentStatus || sale.status),
    amountReceived: Number(sale.amountReceived || sale.amountPaid || 0),
    amountPaid: Number(sale.amountPaid || sale.amountReceived || 0),
    notes: sale.notes || null,
  }
}

function normalizeSalePaymentStatus(status: string): string {
  if (!status) return 'PENDING'
  const s = status.toUpperCase().trim()
  if (s === 'PAID' || s === 'RECEIVED' || s === 'CONFIRMED') return 'RECEIVED'
  if (s === 'PARTIAL') return 'PARTIAL'
  if (s === 'UNPAID' || s === 'PENDING') return 'PENDING'
  return 'PENDING'
}

function normalizePurchaseData(purchase: any) {
  return {
    date: purchase.date ? new Date(purchase.date) : new Date(),
    invoiceNumber: purchase.invoiceNumber || `AI-P-${Date.now()}-${Math.random().toString(36).slice(2, 6)}`,
    partyName: purchase.partyName || 'Unknown Supplier',
    partyAddress: purchase.partyAddress || purchase.partyAddress || null,
    partyGst: purchase.partyGst || purchase.partyGstin || null,
    items: typeof purchase.items === 'string' ? purchase.items : JSON.stringify(purchase.items || []),
    subtotal: Number(purchase.subtotal || purchase.total || 0),
    gstAmount: Number(purchase.gstAmount || purchase.gstTotal || 0),
    totalAmount: Number(purchase.totalAmount || purchase.grandTotal || purchase.total || 0),
    paymentStatus: normalizePurchasePaymentStatus(purchase.paymentStatus || purchase.status),
    amountPaid: Number(purchase.amountPaid || 0),
    notes: purchase.notes || null,
  }
}

function normalizePurchasePaymentStatus(status: string): string {
  if (!status) return 'UNPAID'
  const s = status.toUpperCase().trim()
  if (s === 'PAID' || s === 'CONFIRMED') return 'PAID'
  if (s === 'PARTIAL') return 'PARTIAL'
  if (s === 'UNPAID' || s === 'PENDING') return 'UNPAID'
  return 'UNPAID'
}

function normalizeProductData(product: any) {
  return {
    name: product.name || 'Unknown Product',
    sku: product.sku || product.barcode || null,
    category: product.category || null,
    hsnCode: product.hsnCode || product.hsn || null,
    unit: product.unit || 'PCS',
    brand: product.brand || null,
    itemType: product.itemType || (product.type === 'RAW_MATERIAL' ? 'RAW_MATERIAL' : 'FINISHED_PRODUCT'),
    purchasePrice: Number(product.purchasePrice || product.purchaseRate || product.costPrice || 0),
    salePrice: Number(product.salePrice || product.saleRate || product.sellingPrice || 0),
    mrp: Number(product.mrp || 0) || null,
    openingStock: Number(product.openingStock || product.stock || 0),
    currentStock: Number(product.currentStock || product.stock || 0),
    minStock: Number(product.minStock || product.reorderLevel || 0),
    gstRate: Number(product.gstRate || 18),
    value: Number(product.value || 0),
  }
}

function normalizeBankTxnData(txn: any) {
  // AI may return amount+type or deposit+withdrawal — normalize to deposit+withdrawal
  let deposit = Number(txn.deposit || 0)
  let withdrawal = Number(txn.withdrawal || 0)
  const amount = Number(txn.amount || 0)

  if (deposit === 0 && withdrawal === 0 && amount !== 0) {
    const type = (txn.type || '').toUpperCase()
    if (type === 'CREDIT' || type === 'DEPOSIT' || type === 'IN' || amount > 0) {
      deposit = Math.abs(amount)
      withdrawal = 0
    } else {
      withdrawal = Math.abs(amount)
      deposit = 0
    }
  }

  return {
    date: txn.date ? new Date(txn.date) : new Date(),
    description: txn.description || txn.narration || 'Imported transaction',
    deposit,
    withdrawal,
    balance: Number(txn.balance || 0),
    reference: txn.reference || txn.ref || null,
    category: txn.category || null,
    bankName: txn.bankName || null,
    accountNumber: txn.accountNumber || txn.accountNo || null,
  }
}

