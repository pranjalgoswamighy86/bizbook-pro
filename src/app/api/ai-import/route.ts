export async function POST(req: NextRequest) {
  try {
    const formData = await req.formData()
    const file = formData.get('file') as File | null
    const tenantId = formData.get('tenantId') as string | null
    const importAction = formData.get('action') as string | null

    if (importAction === 'apply' && tenantId) {
      const importDataStr = formData.get('importData') as string
      if (!importDataStr) {
        return NextResponse.json({ error: 'No import data provided' }, { status: 400 })
      }
      return applyImportData(tenantId, JSON.parse(importDataStr))
    }

    // Export merged data to Excel
    if (importAction === 'export-excel' && tenantId) {
      const importDataStr = formData.get('importData') as string
      if (!importDataStr) {
        return NextResponse.json({ error: 'No export data provided' }, { status: 400 })
      }
      return exportMergedExcel(tenantId, JSON.parse(importDataStr))
    }

    if (!file) {
      return NextResponse.json({ error: 'No file uploaded' }, { status: 400 })
    }

    await ensureUploadDir()
    const fileId = randomUUID()
    const fileExt = file.name.split('.').pop() || 'bin'
    const savedFileName = `${fileId}.${fileExt}`
    const filePath = join(UPLOAD_DIR, savedFileName)
    const buffer = Buffer.from(await file.arrayBuffer())
    await writeFile(filePath, buffer)

    console.log(`[AI-Import] Analyzing: ${file.name} (${(buffer.length / 1024).toFixed(1)} KB)`)

    // Fetch tenant info to help AI distinguish sale vs purchase invoices
    let tenantInfo = ''
    let tenantGSTIN = ''
    let tenantName = ''
    if (tenantId) {
      try {
        const tenant = await db.tenant.findUnique({ where: { id: tenantId } })
        if (tenant) {
          tenantGSTIN = tenant.gstNumber || ''
          tenantName = tenant.name || ''
          tenantInfo = `The user's business name is "${tenant.name}"${tenant.gstNumber ? ` with GSTIN ${tenant.gstNumber}` : ''}. Use this to determine if an invoice is a sale (seller = user's business) or purchase (buyer = user's business). IMPORTANT DEFAULT: When uncertain whether an invoice is a sale or purchase, classify it as "purchase_invoice" — because BizBook Pro generates sale invoices internally, and users only import external documents which are almost always purchase invoices from suppliers. Only classify as "sale_invoice" if you are confident the user's business is the seller.`
        }
      } catch (e) {
        console.error('[AI-Import] Failed to fetch tenant for classification context:', e)
      }
    }
    const analysis = await analyzeFileWithAI(file, buffer, tenantInfo)

    // GSTIN-based cross-validation: Override AI classification if GSTINs prove otherwise
    // This is the SAFETY NET — even if AI misclassifies, we can correct based on
    // the actual GSTIN/company name found in the document vs the user's business.
    if (analysis && tenantId) {
      const sellerGst = (analysis.detectedGSTIN || '').trim().toUpperCase()
      const sellerName = (analysis.detectedBusiness || '').trim().toLowerCase()
      const tenantGstNorm = tenantGSTIN.trim().toUpperCase()
      const tenantNameNorm = tenantName.trim().toLowerCase()

      // Helper: check if two business names are similar (fuzzy match)
      const namesMatch = (a: string, b: string): boolean => {
        if (!a || !b) return false
        // Normalize: remove common suffixes and punctuation
        const normalize = (s: string) => s.toLowerCase()
          .replace(/(pvt|private|limited|ltd|llp|inc|co|company|industries|traders|enterprises|corporation|corp|&|and|,|\.|-)/g, '')
          .replace(/\s+/g, '')
          .trim()
        const na = normalize(a)
        const nb = normalize(b)
        if (!na || !nb) return false
        // Exact match after normalization
        if (na === nb) return true
        // One contains the other (e.g., "test business" inside "test business pvt ltd")
        if (na.includes(nb) || nb.includes(na)) return true
        // Both contain at least 6 chars and share a common prefix
        if (na.length >= 6 && nb.length >= 6 && na.substring(0, 6) === nb.substring(0, 6)) return true
        return false
      }

      // v4.9: Spec Section "AI Smart Import Engine" Case C — Bank Statement Mismatch Handling
      // "When processing an uploaded bank statement, the system must run a fuzzy string
      //  comparison check between the statement header's account holder name and the
      //  registered Business Name field in the system settings.
      //  Mismatch Rule: If a variance is detected, do not reject the file.
      //  Intelligent User Prompt: Display a notification banner with action triggers."
      if (analysis.detectedDocumentType === 'bank_statement' && analysis.detectedBusiness) {
        const accountHolderName = analysis.detectedBusiness.trim()
        if (!namesMatch(accountHolderName, tenantName) && accountHolderName.length > 2) {
          analysisResult.warnings.push(
            `BANK_STATEMENT_NAME_MISMATCH: Account holder name on statement ("${accountHolderName}") does not match your company name ("${tenantName}"). ` +
            `This may be the legal Owner/Proprietor name, or an incorrect statement. ` +
            `You can bypass this warning and route to Bank Reconciliation, or assign to Proprietor/Owner Capital Accounts.`
          )
          // Tag the analysis so frontend can show the action triggers
          ;(analysisResult as any).bankStatementMismatch = {
            accountHolderName,
            tenantName,
            actions: [
              { id: 'bypass_reconcile', label: 'Route to Bank Reconciliation Engine', description: 'Bypasses verification to match operational receipts' },
              { id: 'assign_capital', label: 'Assign to Proprietor/Owner Capital Accounts', description: 'Routes entries directly to general journal items' },
            ],
          }
        }
      }

      // Helper: move data from sales array to purchases array (or vice versa)
      const moveSalesToPurchases = () => {
        if (analysis.importData?.sales?.length && !analysis.importData.purchases?.length) {
          analysis.importData.purchases = analysis.importData.sales
          analysis.importData.sales = []
        }
      }
      const movePurchasesToSales = () => {
        if (analysis.importData?.purchases?.length && !analysis.importData.sales?.length) {
          analysis.importData.sales = analysis.importData.purchases
          analysis.importData.purchases = []
        }
      }

      // RULE 1: Seller GSTIN matches tenant's GSTIN → SALE
      // (User's business is the seller — they generated this invoice)
      if (tenantGstNorm && sellerGst && sellerGst === tenantGstNorm) {
        if (analysis.detectedDocumentType === 'purchase_invoice') {
          analysis.warnings = analysis.warnings || []
          analysis.warnings.push(`GSTIN verification: Seller GSTIN matches your business — reclassified from Purchase to Sale invoice.`)
          analysis.detectedDocumentType = 'sale_invoice'
          movePurchasesToSales()
        }
      }
      // RULE 2: Seller GSTIN is DIFFERENT from tenant's GSTIN → PURCHASE
      // (User's business is NOT the seller — they bought from this supplier)
      else if (tenantGstNorm && sellerGst && sellerGst !== tenantGstNorm) {
        if (analysis.detectedDocumentType === 'sale_invoice') {
          analysis.warnings = analysis.warnings || []
          analysis.warnings.push(`GSTIN verification: Seller GSTIN does not match your business — reclassified from Sale to Purchase invoice.`)
          analysis.detectedDocumentType = 'purchase_invoice'
          moveSalesToPurchases()
        }
      }
      // RULE 3: No GSTIN detected, but seller NAME matches tenant name → SALE
      else if (!sellerGst && tenantNameNorm && sellerName && namesMatch(sellerName, tenantNameNorm)) {
        if (analysis.detectedDocumentType === 'purchase_invoice') {
          analysis.warnings = analysis.warnings || []
          analysis.warnings.push(`Business name verification: Seller name matches your business — reclassified from Purchase to Sale invoice.`)
          analysis.detectedDocumentType = 'sale_invoice'
          movePurchasesToSales()
        }
      }
      // RULE 4: No GSTIN detected, but seller NAME is DIFFERENT from tenant name → PURCHASE
      // (Critical fix: previously this case was missing — caused purchase invoices
      // to be misclassified as sale invoices when AI couldn't extract GSTIN)
      else if (!sellerGst && tenantNameNorm && sellerName && !namesMatch(sellerName, tenantNameNorm) && sellerName.length >= 3) {
        if (analysis.detectedDocumentType === 'sale_invoice') {
          analysis.warnings = analysis.warnings || []
          analysis.warnings.push(`Business name verification: Seller name "${analysis.detectedBusiness}" does not match your business "${tenantName}" — reclassified from Sale to Purchase invoice.`)
          analysis.detectedDocumentType = 'purchase_invoice'
          moveSalesToPurchases()
        }
      }
      // RULE 5: Default safety net — if no GSTIN and no business name detected,
      // and AI said "sale_invoice", downgrade confidence and add a warning.
      // BizBook Pro generates sale invoices internally, so importing a "sale"
      // invoice is suspicious — user is likely importing a supplier's invoice.
      else if (!sellerGst && !sellerName && analysis.detectedDocumentType === 'sale_invoice') {
        analysis.warnings = analysis.warnings || []
        analysis.warnings.push(`Could not verify seller identity. Defaulting to PURCHASE invoice (BizBook Pro generates sale invoices internally — imported files are typically supplier purchase invoices).`)
        analysis.detectedDocumentType = 'purchase_invoice'
        moveSalesToPurchases()
      }
    }

    // v4.10: Spec Section 2 Case A — Inventory Cross-Reference Rule
    // "For each extracted line item, the system must search the Products table.
    //  If the name matches an existing string, bind it to its current SKU.
    //  If it does not exist, flag it visually as a [ New Product Detected ] block."
    if (tenantId && analysis.importData) {
      const allLineItems = [
        ...(analysis.importData.sales || []).flatMap((s: any) => s.items || []),
        ...(analysis.importData.purchases || []).flatMap((p: any) => p.items || []),
        ...(analysis.importData.products || []),
      ]

      if (allLineItems.length > 0 && allLineItems.length < 200) {
        try {
          // Get all existing inventory items for this tenant
          const existingItems = await db.inventoryItem.findMany({
            where: { tenantId, isDeleted: false },
            select: { id: true, name: true, sku: true, hsnCode: true },
          })

          const normalizeName = (s: string) => (s || '').toLowerCase().trim().replace(/\s+/g, ' ')

          // Tag each line item with match status
          let newProductCount = 0
          let matchedCount = 0
          for (const item of allLineItems) {
            const itemName = normalizeName(item.name || item.productName || item.description || '')
            if (!itemName) continue

            const match = existingItems.find((ei) => {
              const eiName = normalizeName(ei.name)
              if (!eiName) return false
              if (eiName === itemName) return true
              if (eiName.length >= 6 && itemName.length >= 6 &&
                  (eiName.includes(itemName) || itemName.includes(eiName))) return true
              return false
            })

            if (match) {
              item._matchedInventoryId = match.id
              item._matchedSku = match.sku
              item._matchStatus = 'EXISTING'
              matchedCount++
            } else {
              item._matchStatus = 'NEW_PRODUCT_DETECTED'
              newProductCount++
            }
          }

          if (newProductCount > 0) {
            analysis.warnings = analysis.warnings || []
            analysis.warnings.push(
              `INVENTORY_CROSS_REF: ${newProductCount} new product(s) detected (not in your inventory). ` +
              `${matchedCount} item(s) matched existing inventory. ` +
              `New products will be flagged as [New Product Detected] in the preview — you can register SKUs and HSN codes before saving.`
            )
            ;(analysis as any).inventoryCrossRef = {
              totalItems: allLineItems.length,
              matched: matchedCount,
              newProducts: newProductCount,
            }
          }
        } catch (err: any) {
          console.warn('[AI-Import] Inventory cross-ref failed:', err?.message)
        }
      }
    }

    return NextResponse.json({ success: true, analysis, savedFileName: file.name })
  } catch (error: any) {
    console.error('[AI-Import] Error:', error)
    return NextResponse.json({
      error: error.message || 'Failed to analyze file',
    }, { status: 500 })
  }
}

