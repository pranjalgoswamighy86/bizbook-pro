async function analyzeFileWithAI(file: File, buffer: Buffer, tenantInfo: string = ''): Promise<any> {
  const zai = await ZAI.create()
  const fileName = file.name.toLowerCase()
  const mimeType = file.type || 'application/octet-stream'
  const systemPrompt = tenantInfo
    ? ANALYSIS_SYSTEM_PROMPT + '\n\nUSER CONTEXT: ' + tenantInfo
    : ANALYSIS_SYSTEM_PROMPT
  let analysisResult: any

  // 1. IMAGES — vision API with base64
  if (mimeType.startsWith('image/') || fileName.match(/\.(png|jpg|jpeg|gif|bmp|webp)$/)) {
    const base64Image = buffer.toString('base64')
    const imageDataUrl = `data:${mimeType};base64,${base64Image}`
    const response = await zai.chat.completions.createVision({
      messages: [
        { role: 'system', content: systemPrompt },
        { role: 'user', content: [
          { type: 'text', text: `Analyze this business document image (${file.name}) and extract all structured data for import into BizBook Pro. Return ONLY valid JSON.` },
          { type: 'image_url', image_url: { url: imageDataUrl } }
        ] }
      ],
      thinking: { type: 'disabled' }
    })
    analysisResult = parseAIResponse(response.choices[0]?.message?.content || '')
  }
  // 2. EXCEL — server-side parsing then AI chat
  else if (fileName.match(/\.(xlsx|xls)$/)) {
    const excelData = await parseExcelFile(buffer)
    if (excelData.includes('[Excel parsing failed')) {
      // Fallback: vision API with file_url
      const base64File = buffer.toString('base64')
      const fileDataUrl = `data:application/vnd.openxmlformats-officedocument.spreadsheetml.sheet;base64,${base64File}`
      try {
        const response = await zai.chat.completions.createVision({
          messages: [
            { role: 'system', content: systemPrompt },
            { role: 'user', content: [
              { type: 'text', text: `Analyze this Excel file (${file.name}) and extract all structured business data. Return ONLY valid JSON.` },
              { type: 'file_url', file_url: { url: fileDataUrl } }
            ] }
          ],
          thinking: { type: 'disabled' }
        })
        analysisResult = parseAIResponse(response.choices[0]?.message?.content || '')
      } catch { analysisResult = null }
    } else {
      const truncatedData = excelData.length > 80000 ? excelData.substring(0, 80000) + '\n... [TRUNCATED]' : excelData
      const response = await zai.chat.completions.create({
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: `Analyze this Excel spreadsheet data (${file.name}) parsed into JSON. Each key is a sheet name with headers and data rows. Extract ALL business data.\n\nParsed data:\n\`\`\`json\n${truncatedData}\n\`\`\`\n\nReturn ONLY valid JSON.` }
        ]
      })
      analysisResult = parseAIResponse(response.choices[0]?.message?.content || '')
    }
  }
  // 3. PDF — server-side text extraction then AI chat (fallback: vision)
  else if (mimeType === 'application/pdf' || fileName.endsWith('.pdf')) {
    const pdfText = await parsePdfFile(buffer)
    if (pdfText && !pdfText.includes('[PDF text extraction failed') && pdfText.length > 50) {
      const truncatedText = pdfText.length > 80000 ? pdfText.substring(0, 80000) + '\n... [TRUNCATED]' : pdfText
      const response = await zai.chat.completions.create({
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: `Analyze this PDF document (${file.name}) with extracted text. Extract ALL business data.\n\nText:\n\`\`\`\n${truncatedText}\n\`\`\`\n\nReturn ONLY valid JSON.` }
        ]
      })
      analysisResult = parseAIResponse(response.choices[0]?.message?.content || '')
    } else {
      const base64Pdf = buffer.toString('base64')
      const pdfDataUrl = `data:application/pdf;base64,${base64Pdf}`
      try {
        const response = await zai.chat.completions.createVision({
          messages: [
            { role: 'system', content: systemPrompt },
            { role: 'user', content: [
              { type: 'text', text: `Analyze this PDF (${file.name}) and extract all structured business data. Return ONLY valid JSON.` },
              { type: 'file_url', file_url: { url: pdfDataUrl } }
            ] }
          ],
          thinking: { type: 'disabled' }
        })
        analysisResult = parseAIResponse(response.choices[0]?.message?.content || '')
      } catch { analysisResult = null }
    }
  }
  // 4. WORD / RTF — server-side mammoth parsing then AI chat
  else if (fileName.match(/\.(docx|doc|rtf|odt)$/)) {
    const docText = await parseWordFile(buffer)
    if (docText && !docText.includes('[Word parsing failed') && !docText.includes('[Could not parse') && docText.length > 10) {
      const truncatedText = docText.length > 80000 ? docText.substring(0, 80000) + '\n... [TRUNCATED]' : docText
      const response = await zai.chat.completions.create({
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: `Analyze this Word/RTF document (${file.name}) with extracted text. This may contain expenses, purchases, party info, or any business data. Extract ALL business data.\n\nText:\n\`\`\`\n${truncatedText}\n\`\`\`\n\nReturn ONLY valid JSON.` }
        ]
      })
      analysisResult = parseAIResponse(response.choices[0]?.message?.content || '')
    } else {
      // Fallback: vision API
      const base64File = buffer.toString('base64')
      const docMimeType = fileName.endsWith('.docx') ? 'application/vnd.openxmlformats-officedocument.wordprocessingml.document'
        : fileName.endsWith('.doc') ? 'application/msword'
        : fileName.endsWith('.rtf') ? 'application/rtf'
        : 'application/vnd.oasis.opendocument.text'
      const fileDataUrl = `data:${docMimeType};base64,${base64File}`
      try {
        const response = await zai.chat.completions.createVision({
          messages: [
            { role: 'system', content: systemPrompt },
            { role: 'user', content: [
              { type: 'text', text: `Analyze this document (${file.name}) and extract ALL business data. Return ONLY valid JSON.` },
              { type: 'file_url', file_url: { url: fileDataUrl } }
            ] }
          ],
          thinking: { type: 'disabled' }
        })
        analysisResult = parseAIResponse(response.choices[0]?.message?.content || '')
      } catch { analysisResult = null }
    }
  }
  // 5. CSV, JSON, XML, TXT, TSV — text-based, send to chat
  else {
    let fileContent = ''
    if (fileName.match(/\.(csv|json|txt|xml|tsv)$/)) {
      fileContent = buffer.toString('utf-8')
    }

    let parsedData = ''
    if (fileName.match(/\.(csv|tsv)$/)) {
      parsedData = parseCSVToStructured(fileContent, fileName.endsWith('.tsv'))
    } else if (fileName.endsWith('.json')) {
      try { parsedData = JSON.stringify(JSON.parse(fileContent), null, 2) } catch { parsedData = fileContent }
    } else if (fileName.endsWith('.xml')) {
      parsedData = fileContent
    } else if (fileName.endsWith('.txt')) {
      parsedData = fileContent
    }

    if (parsedData) {
      const truncatedData = parsedData.length > 80000 ? parsedData.substring(0, 80000) + '\n... [TRUNCATED]' : parsedData
      const response = await zai.chat.completions.create({
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: `Analyze this data file (${file.name}, ${buffer.length} bytes) and extract all structured business data.\n\nContent:\n\`\`\`\n${truncatedData}\n\`\`\`\n\nReturn ONLY valid JSON.` }
        ]
      })
      analysisResult = parseAIResponse(response.choices[0]?.message?.content || '')
    } else {
      // Unknown binary — try vision API
      const base64File = buffer.toString('base64')
      const fileDataUrl = `data:${mimeType};base64,${base64File}`
      try {
        const response = await zai.chat.completions.createVision({
          messages: [
            { role: 'system', content: systemPrompt },
            { role: 'user', content: [
              { type: 'text', text: `Analyze this file (${file.name}, ${mimeType}) and extract business data. Return ONLY valid JSON.` },
              { type: 'file_url', file_url: { url: fileDataUrl } }
            ] }
          ],
          thinking: { type: 'disabled' }
        })
        analysisResult = parseAIResponse(response.choices[0]?.message?.content || '')
      } catch { analysisResult = null }
    }
  }

  if (!analysisResult) {
    analysisResult = {
      fileName: file.name, fileType: 'unknown',
      summary: 'Could not analyze this file. Try CSV, Excel, or image format.',
      confidence: 0, importData: {},
      warnings: ['AI analysis could not parse the file'],
      suggestions: ['Try CSV, JSON, or image format', 'Ensure Excel files are .xlsx', 'For PDFs, use text-based rather than scanned']
    }
  }

  analysisResult.fileName = analysisResult.fileName || file.name
  analysisResult.importData = analysisResult.importData || {}
  analysisResult.warnings = analysisResult.warnings || []
  analysisResult.suggestions = analysisResult.suggestions || []
  // Ensure detectedDocumentType is present; infer from fileType if missing
  // IMPORTANT: Do NOT map generic "invoice" to "sale_invoice" — an invoice
  // could be a sale OR a purchase. Use importData to disambiguate.
  if (!analysisResult.detectedDocumentType) {
    const ft = (analysisResult.fileType || '').toLowerCase()

    // First, check importData arrays to determine the actual type from data
    const populatedKeys = Object.keys(analysisResult.importData || {}).filter(k => Array.isArray(analysisResult.importData[k]) && analysisResult.importData[k].length > 0)
    const hasSales = populatedKeys.includes('sales')
    const hasPurchases = populatedKeys.includes('purchases')
    const dataMap: Record<string, string> = { sales: 'sale_invoice', purchases: 'purchase_invoice', bankTransactions: 'bank_statement', products: 'inventory_data', expenses: 'expense_data', staff: 'staff_data', parties: 'party_data' }

    if (ft.includes('sale') && !ft.includes('purchase')) {
      // fileType says "sale" — but verify with importData, as AI often misclassifies
      // purchase invoices as "sale" type. If purchases data exists, it's likely a purchase.
      if (hasPurchases && !hasSales) {
        analysisResult.detectedDocumentType = 'purchase_invoice'
        analysisResult.warnings.push('File type indicated "sale" but data suggests this is a Purchase invoice. Reclassified accordingly.')
      } else if (hasSales) {
        // Sales data exists — classify as sale, but cross-validation in the
        // main handler will override this if GSTIN/name doesn't match tenant.
        analysisResult.detectedDocumentType = 'sale_invoice'
      } else {
        // No sales AND no purchases data — default to PURCHASE because
        // sale invoices are GENERATED by BizBook Pro (not imported).
        // Users import external supplier invoices, which are PURCHASE invoices.
        analysisResult.detectedDocumentType = 'purchase_invoice'
        analysisResult.warnings.push('File type indicated "sale" but no sale data was extracted. Defaulted to Purchase invoice (BizBook Pro generates sale invoices internally — imported files are typically supplier purchase invoices).')
      }
    } else if (ft.includes('purchase') || ft.includes('supplier')) {
      analysisResult.detectedDocumentType = 'purchase_invoice'
    } else if (ft.includes('bank')) {
      analysisResult.detectedDocumentType = 'bank_statement'
    } else if (ft.includes('product') || ft.includes('inventory')) {
      analysisResult.detectedDocumentType = 'inventory_data'
    } else if (ft.includes('expense')) {
      analysisResult.detectedDocumentType = 'expense_data'
    } else if (ft.includes('tally') || ft.includes('backup')) {
      analysisResult.detectedDocumentType = 'backup_data'
    } else if (ft.includes('mixed')) {
      analysisResult.detectedDocumentType = 'mixed_data'
    } else if (ft.includes('invoice')) {
      // Generic "invoice" — ambiguous. Default to PURCHASE because:
      // - Sale invoices are GENERATED by BizBook Pro (user doesn't import them)
      // - Users IMPORT external supplier invoices, which are PURCHASE invoices
      // - Only classify as sale_invoice if tenant GSTIN matches the seller
      if (hasPurchases && !hasSales) {
        analysisResult.detectedDocumentType = 'purchase_invoice'
      } else if (hasSales && !hasPurchases) {
        // AI put data in sales array — but this could be wrong (common misclassification).
        // Still classify as sale_invoice but add a warning for user verification.
        analysisResult.detectedDocumentType = 'sale_invoice'
        analysisResult.warnings.push('Invoice classified as Sale — please verify this is not a Purchase invoice from a supplier. Sale invoices are normally generated by BizBook Pro, not imported.')
      } else if (hasSales && hasPurchases) {
        // Both sales AND purchases present — don't guess
        analysisResult.detectedDocumentType = 'mixed_data'
      } else {
        // No sales or purchase data to infer from — default to purchase
        // because imported invoices are almost always from external suppliers
        analysisResult.detectedDocumentType = 'purchase_invoice'
        analysisResult.warnings.push('Invoice type ambiguous — defaulted to Purchase. Imported invoices are typically from external suppliers. Please verify manually.')
      }
    } else {
      // No fileType match — infer from populated importData keys
      if (populatedKeys.length === 1) {
        analysisResult.detectedDocumentType = dataMap[populatedKeys[0]] || 'unknown'
      } else if (populatedKeys.length > 1) {
        analysisResult.detectedDocumentType = 'mixed_data'
      } else {
        analysisResult.detectedDocumentType = 'unknown'
      }
    }
  }
  return analysisResult
}

function parseAIResponse(content: string): any {
  let jsonStr = content.trim()
  const codeBlockMatch = jsonStr.match(/```(?:json)?\s*([\s\S]*?)```/)
  if (codeBlockMatch) jsonStr = codeBlockMatch[1].trim()
  try { return JSON.parse(jsonStr) } catch {
    const jsonMatch = jsonStr.match(/\{[\s\S]*\}/)
    if (jsonMatch) {
      try { return JSON.parse(jsonMatch[0]) } catch {
        try { return JSON.parse(jsonMatch[0].replace(/,\s*([}\]])/g, '$1')) } catch { return null }
      }
    }
    return null
  }
}

